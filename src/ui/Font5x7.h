template<class T>
static const uint8_t* iqGlyph(T c) {
  for (auto &g : IQ_FONT) if (g.c == c) return g.p;
  return nullptr;
}

static char32_t iqNextUtf8(const char*& s) {
  uint8_t c = (uint8_t)*s++;
  if (c < 0x80) return c;
  if ((c >> 5) == 6) return ((c & 0x1F) << 6) | ((uint8_t)*s++ & 0x3F);
  if ((c >> 4) == 0xE) {
    char32_t r = c & 0x0F;
    r = (r << 6) | ((uint8_t)*s++ & 0x3F);
    r = (r << 6) | ((uint8_t)*s++ & 0x3F);
    return r;
  }
  return U'?';
}

template<class GFX>
static void iqDrawChar(GFX& gfx, int x, int y, char32_t c, uint16_t fg, uint16_t bg, int s) {
  const uint8_t* p = iqGlyph(c);
  static const uint8_t box[5] = {0x7F,0x41,0x5D,0x41,0x7F};
  if (!p) p = box;
  for (int cx=0; cx<5; cx++) {
    for (int cy=0; cy<7; cy++) {
      gfx.fillRect(x + cx*s, y + cy*s, s, s, (p[cx] & (1 << cy)) ? fg : bg);
    }
  }
  gfx.fillRect(x + 5*s, y, s, 7*s, bg);
}

template<class GFX>
static void iqDrawText(GFX& gfx, int x, int y, const char* txt, uint16_t fg, uint16_t bg, int s) {
  int cx = x;
  const char* p = txt;
  while (*p) {
    char32_t c = iqNextUtf8(p);
    if (c == '\n') { y += 9*s; cx = x; continue; }
    iqDrawChar(gfx, cx, y, c, fg, bg, s);
    cx += 6*s;
  }
}

template<class GFX>
static void iqDrawTextClipped(GFX& gfx, int x, int y, const char* txt,
                              uint16_t fg, uint16_t bg, int s,
                              int clipX, int clipY, int clipW, int clipH) {
  const int clipR = clipX + clipW;
  const int clipB = clipY + clipH;
  int cx = x;
  const char* p = txt;
  while (*p) {
    char32_t c = iqNextUtf8(p);
    if (c == '\n') { y += 9*s; cx = x; continue; }
    const uint8_t* glyph = iqGlyph(c);
    static const uint8_t box[5] = {0x7F,0x41,0x5D,0x41,0x7F};
    if (!glyph) glyph = box;
    if (cx + 6*s > clipX && cx < clipR && y + 7*s > clipY && y < clipB) {
      for (int gx=0; gx<6; ++gx) {
        for (int gy=0; gy<7; ++gy) {
          int px = cx + gx*s;
          int py = y + gy*s;
          int x1 = px < clipX ? clipX : px;
          int y1 = py < clipY ? clipY : py;
          int x2 = (px+s) > clipR ? clipR : (px+s);
          int y2 = (py+s) > clipB ? clipB : (py+s);
          if (x2 <= x1 || y2 <= y1) continue;
          const bool on = gx < 5 && (glyph[gx] & (1 << gy));
          gfx.fillRect(x1, y1, x2-x1, y2-y1, on ? fg : bg);
        }
      }
    }
    cx += 6*s;
  }
}

static int iqTextWidthPx(const char* txt, int s) {
  int chars = 0;
  const char* p = txt;
  while (*p) {
    char32_t c = iqNextUtf8(p);
    if (c != '\n') ++chars;
  }
  return chars * 6 * s;
}
